#include "libft.h"

void	ft_putnbr_fd(int nbr, int fd)
{
	if (nbr == INT_MIN)
	{
		ft_putstr_fd("-2147483648", fd);
		return ;
	}
	if (nbr < 0)
	{
		ft_putchar_fd('-', fd);
		nbr *= -1;
	}
	if (nbr >= 10)
	{
		ft_putnbr_fd((nbr / 10), fd);
		ft_putnbr_fd((nbr % 10), fd);
	}
	else if (nbr <= 9)
	{
		ft_putchar_fd((nbr + '0'), fd);
	}
}
